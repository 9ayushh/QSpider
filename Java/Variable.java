public class Variable {
    public static void main(String[] args) {
        // syntax: data_type var_name = value;
        
        System.out.println("start");
        
        // byte type 3 var
        byte roll;
        byte roomno;
        byte slno;
        byte age;
        byte classno;
        // initialization
        roll = 4;
        roomno = 101;
        slno = 50;
        age = 20;
        classno = 12;
        System.out.println(roll);
        System.out.println("Byte Variable Initialized");


        // for short variable
        short id;
        short marks;
        short year;
        short modelno;
        short vacancy;
        // Initialization
        id = 10000;
        marks = 85;
        year = 2024;
        modelno = 20204;
        vacancy = 500;
        System.out.println(id);
        System.out.println("Short Variable Initialized");

        // for int variable
        int population;
        int score;
        int pin;
        int amount;
        int balance;
        // initialization
        population = 1000000;
        score = 85;
        pin = 123456;
        amount = 1000000;
        balance = 50009904;
        System.out.println(population);
        System.out.println("Int Variable Initialized");

        // for long variable
        long distanceToSun;
        long distanceToMoon;
        long aadharNumber;
        long account;
        long nationalDebt;
        // initialization
        distanceToSun = 149600000L;
        distanceToMoon = 384400L;
        aadharNumber = 123456789012L;
        account = 1234567890123456L;
        nationalDebt = 28000000000000L;
        System.out.println(distanceToSun);
        System.out.println("Long Variable Initialized");
        
        // for float variable
        float size;
        float price;
        float weigth;
        float height;
        float width;
        // initialization
        size = 10.5f;
        price = 99.99f;
        weigth = 5.5f;
        height = 1.75f;
        width = 2.0f;
        System.out.println(size);
        System.out.println("Float Variable Initialized");

        // for double variable
        double pi;
        double gravity;
        double speedOfLight;
        double avogadro;
        double planck;
        // initialization
        pi = 3.14159;
        gravity = 9.81;
        speedOfLight = 299792458;
        avogadro = 6.02214076e23;
        planck = 6.62607015e-34;
        System.out.println(pi);
        System.out.println("Double Variable Initialized");

        // for character variable
        // grade
        char grade;
        // section
        char section;
        // block
        char block;
        // gender
        char gender;
        // group
        char group;
        // initialization
        grade = 'A';
        section = 'B';
        block = 'C';
        gender = 'M';
        group = 'X';
        System.out.println(grade);
        System.out.println("Char Variable Initialized");
        
        // boolean
        // is possible
        boolean isPossible;
        // is available
        boolean isAvailable;
        // is valid
        boolean isValid;
        // is empty
        boolean isEmpty;
        // is full
        boolean isFull;
        // initialization
        isPossible = true;
        isAvailable = false;
        isValid = true;
        isEmpty = false;
        isFull = true;
        System.out.println(isPossible);
        System.out.println("Boolean Variable Initialized");


        System.out.println("All Variables are initialized");
        
    }
}
